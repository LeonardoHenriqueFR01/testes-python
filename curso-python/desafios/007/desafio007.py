# CALCULANDO A MÉDIA DE 2 NOTAS desafio 007


nota_1 = float(input('PRIMEIRA NOTA: '))
nota_2 = float(input('SEGUNDA NOTA: '))

media = (nota_1 + nota_2) / 2

print(f'PRIMEIRA NOTA FOI: {nota_1}')
print(f'SEGUNDA NOTA FOI: {nota_2}')
print(f'A SOMA DAS DUAS NOTAS FICARAM: {nota_1 + nota_2}')
print(f'A MÉDIA DAS DUAS NOTAS SÃO: {media}')
